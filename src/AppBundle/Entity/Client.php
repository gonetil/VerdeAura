<?php

namespace AppBundle\Entity;

use Doctrine\ORM\Mapping as ORM;

/**
 * Client
 *
 * @ORM\Table(name="client")
 * @ORM\Entity(repositoryClass="AppBundle\Repository\ClientRepository")
 */
class Client
{
    /**
     * @var int
     *
     * @ORM\Column(name="id", type="integer")
     * @ORM\Id
     * @ORM\GeneratedValue(strategy="AUTO")
     */
    private $id;

    /**
     * @var string
     *
     * @ORM\Column(name="name", type="string", length=255)
     */
    private $name;

    /**
     * @var float
     *
     * @ORM\Column(name="automaticDiscount", type="float")
     */
    private $automaticDiscount;


    /**
     * Get id
     *
     * @return int
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set name
     *
     * @param string $name
     *
     * @return Client
     */
    public function setName($name)
    {
        $this->name = $name;

        return $this;
    }

    /**
     * Get name
     *
     * @return string
     */
    public function getName()
    {
        return $this->name;
    }

    /**
     * Set automaticDiscount
     *
     * @param float $automaticDiscount
     *
     * @return Client
     */
    public function setAutomaticDiscount($automaticDiscount)
    {
        $this->automaticDiscount = $automaticDiscount;

        return $this;
    }

    /**
     * Get automaticDiscount
     *
     * @return float
     */
    public function getAutomaticDiscount()
    {
        return $this->automaticDiscount;
    }
}

